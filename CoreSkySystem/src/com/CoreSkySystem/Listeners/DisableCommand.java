package com.CoreSkySystem.Listeners;

import org.bukkit.entity.Player;
import org.bukkit.event.EventHandler;
import org.bukkit.event.EventPriority;
import org.bukkit.event.Listener;
import org.bukkit.event.player.PlayerCommandPreprocessEvent;

import com.CoreSkySystem.Main.Main;

public class DisableCommand implements Listener  {
	
	
	@EventHandler(priority=EventPriority.HIGHEST)
	public void onChat(PlayerCommandPreprocessEvent event)  {
		String msg = event.getMessage();
		Player p = event.getPlayer();
		/** 
		 *  geblockte Commands
		 */
		if(p.hasPermission("*")){
			event.setCancelled(false);
		} else {
			 if(msg.startsWith("/pl"))  { 
					event.setCancelled(true);
					p.sendMessage("Plugins (7); 브SkyFightAntiHack-System븑, 브WorldEdit븑, 브WorldGuard븑, 브NoCheatPlus븑, 브GroupManager븑, 브Multivers-Core븑, 브Essentials");
					
				}
		}
		if(p.hasPermission("*")){
			event.setCancelled(false); 
		} else {
			if(msg.startsWith("/?"))  { 
				event.setCancelled(true);
				p.sendMessage(Main.name + " 븎Alle wichtigen Befehle findest du unter 븎/Hilfe");
			}
		}
		
		if(p.hasPermission("*")){
			event.setCancelled(false); 
		} else {
			if(msg.startsWith("/Bukkit:me"))  { 
				event.setCancelled(true);
				p.sendMessage(Main.name +" 븎Aha, Ban?");
			}
		}	
	}

}
